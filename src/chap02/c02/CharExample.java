package chap02.c02;

public class CharExample {
    public static void main(String[] args) {
        char c1 = 'A'; // 문자로 직접 저장 
        char c2 = 65; // 10진수로 저장 
        char c3 = '\u0041'; // 16진수로 저장 

        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
    }
}
